#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	index;

	if (s != NULL && f != NULL)
	{
		index = 0;
		while (s[index] != '\0')
		{
			f(&s[index]);
			index++;
		}
	}
}
